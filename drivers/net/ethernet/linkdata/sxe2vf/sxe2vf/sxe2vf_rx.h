static inline bool sxe2vf_is_non_eop(struct sxe2vf_queue *rxq,
				     union sxe2vf_rx_desc *rx_desc)
{
	if (likely(sxe2vf_desc_status0_err_test(rx_desc->wb.status0_err,
						BIT(SXE2VF_RX_DESC_STATUS0_EOP))))
		return false;

	return true;
}

static inline u8 sxe2vf_rx_desc_lro_cnt(struct sxe2vf_queue *rxq,
					union sxe2vf_rx_desc *rx_desc)
{
	if (test_bit(SXE2VF_RXQ_LRO_ENABLED, &rxq->flags)) {
		return (le32_to_cpu(rx_desc->wb.status_lrocnt_fdpf_id) >>
			SXE2VF_RX_DESC_LROCNT_SHIFT) &
		       SXE2VF_RX_DESC_LROCNT_MASK;
	}

	return 0;
}

static inline u16 sxe2vf_rx_offset_get(struct sxe2vf_queue *rxq)
{
	return test_bit(SXE2VF_RXQ_BUILD_SKB_ENABLED, &rxq->flags) ?
		       SXE2VF_SKB_PAD_VALUE :
		       0;
}

