static inline u32 sxe2vf_rx_page_order(struct sxe2vf_queue *rxq)
{
	u32 order = 0;

#if (PAGE_SIZE < 8192)
	if (rxq->rx_buf_len > (PAGE_SIZE / 2))
		order = 1;
#endif
	return order;
}

#define SXE2VF_RX_PAGE_ORDER(rxq) (sxe2vf_rx_page_order(rxq))
#define SXE2VF_RX_PAGE_SIZE(rxq) (PAGE_SIZE << SXE2VF_RX_PAGE_ORDER(rxq))

